const now = Date.now();
const min = 60 * 1000;

function calcScore(level: TicketLevel, elapsed: number): number {
  const weights: Record<TicketLevel, number> = { P0: 100, P1: 60, P2: 25, P3: 10 };
  return weights[level] + Math.floor(elapsed / (5 * min));
}

function mkLog(id: string, ts: number, action: string, opId: string, opName: string, remark?: string) {
  return { id, timestamp: ts, action, operatorId: opId, operatorName: opName, remark };
}

