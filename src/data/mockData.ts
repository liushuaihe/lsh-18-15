export function syncPersonnelWithTickets(personnel: Personnel[], tickets: Ticket[]): Personnel[] {
  return personnel.map(p => {
    const assigned = tickets.filter(t =>
      (t.status === 'assigned' || t.status === 'working' || t.status === 'verifying') &&
      t.assigneeId === p.id
    );
    const status: Personnel['status'] =
      assigned.length >= 2 ? 'busy' :
      p.status === 'offline' || p.status === 'away' ? p.status :
      assigned.length === 1 ? 'busy' : 'idle';
    return {
      ...p,
      status,
      currentTicketIds: assigned.map(t => t.id),
    };
  });
}
